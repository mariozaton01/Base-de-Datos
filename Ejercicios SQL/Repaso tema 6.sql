SELECT D.DEPT_NO, D.DNOMBRE
FROM DEPART D, EMPLE E
WHERE D.DEPT_NO=E.DEPT_NO
AND (SELECT DEPT_NO FROM EMPLE
WHERE MAX(OFICIO) IN 'PROGRAMADOS')
GROUP BY D.DEPT_NO, D.DNOMBRE; 
                
                