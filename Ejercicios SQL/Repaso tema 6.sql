SELECT D.DEPT_NO, D.DNOMBRE
FROM DEPART D
WHERE D.DEPT_NO IN(SELECT DEPT_NO
                 FROM EMPLE
                 WHERE COMISION_PCT IS NOT NULL)
GROUP BY DEPT_NO, DNOMBRE;

