SELECT DEPT_NO, DNOMBRE
FROM DEPART
WHERE DEPT_NO NOT IN(SELECT DEPT_NO
                    FROM EMPLE
                    WHERE COMISION_PCT IS NOT NULL)
GROUP BY DEPT_NO, DNOMBRE;


